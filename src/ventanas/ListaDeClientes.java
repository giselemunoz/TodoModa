    private void jbBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBorrarActionPerformed
        // TODO add your handling code here:
                int fila = JtClientes.getSelectedRow();

        String valor = JtClientes.getValueAt(fila, 0).toString();

        try {
            Conexion cc = new Conexion();
           
            Connection cn = cc.ConectarBasedeDatos();
            String sql = "UPDATE cliente SET  activo  = '0' WHERE idCliente = '" + valor + "' ";
            PreparedStatement eliminar = cn.prepareStatement(sql);
            eliminar.executeUpdate();
            JOptionPane.showConfirmDialog(null, "¿Desea borrar el cliente?");
            JOptionPane.showMessageDialog(null, "El cliente ha sido borrado con exito");

        } catch (SQLException e) {
     
        }
        
    }//GEN-LAST:event_jbBorrarActionPerformed

