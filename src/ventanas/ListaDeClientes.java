    private void txtBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyPressed
        // TODO add your handling code here:
        String[] nombres = {"Codigo", "Nombre", "Apellido", "Cuit"};
        String[] registro = new String[30];
        String valor = txtBuscar.getText();

        DefaultTableModel model = new DefaultTableModel(null, nombres);

        try {
            resultado = clienteDAO.search(valor);

            while (resultado.next()) {
                registro[0] = resultado.getString("idCliente");
                registro[1] = resultado.getString("nombre");
                registro[2] = resultado.getString("apellido");
                registro[3] = resultado.getString("cuit");
                model.addRow(registro);
            }
            JtClientes.setModel(model);

        } catch (SQLException ex) {
            Logger.getLogger(ListaDeProveedores.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtBuscarKeyPressed

