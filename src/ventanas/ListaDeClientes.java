    private void JtClientesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JtClientesKeyReleased
        // TODO add your handling code here:
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            Cliente cli = new Cliente();
            int codigo =Integer.parseInt( JtClientes.getValueAt(JtClientes.getSelectedRow(),0).toString());
            String nombre = JtClientes.getValueAt(JtClientes.getSelectedRow(), 1).toString();
            String apellido = JtClientes.getValueAt(JtClientes.getSelectedRow(), 2).toString();
            String cuit = JtClientes.getValueAt(JtClientes.getSelectedRow(), 3).toString();
           
            cli.setIdCliente(codigo);
            cli.setNombre(nombre);
            cli.setApellido(apellido);
            cli.setCuit(cuit);
            clienteDAO.update(cli);
        
            JOptionPane.showMessageDialog(null, "El cliente  fue modificado con éxito");

        }
    }//GEN-LAST:event_JtClientesKeyReleased


